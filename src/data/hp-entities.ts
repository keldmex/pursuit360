export function getEntitiesByCountry(isoOrName: string): HPLegalEntity[] {
  const q = isoOrName.trim().toUpperCase()
  return HP_LEGAL_ENTITIES.filter(e =>
    e.iso_alpha2.toUpperCase() === q ||
    e.country.trim().toUpperCase() === isoOrName.trim().toUpperCase()
  )
}

/** Get the correct contracting entity based on country + deal type */
export function getContractingEntity(
  isoOrName: string,
  opts: { isIndigo?: boolean; isFederalUS?: boolean; rtm?: 'Direct' | 'Indirect' } = {}
): HPLegalEntity | null {
  const entries = getEntitiesByCountry(isoOrName)
  if (!entries.length) return null

  // US Federal override
  if (opts.isFederalUS) {
    const fed = entries.find(e => e.contracting_entity === 'HPI Federal LLC')
    if (fed) return fed
  }

  // Indigo override
  if (opts.isIndigo) {
    const indigo = entries.find(e => e.indigo_eligible)
    if (indigo) return indigo
  }

  // RTM-specific preference
  if (opts.rtm) {
    const rtmMatch = entries.find(e =>
      e.rtm === opts.rtm || e.rtm === 'Both'
    )
    if (rtmMatch) return rtmMatch
  }

  // Default: first non-Indigo entity
  return entries.find(e => !e.indigo_eligible) ?? entries[0]
}

/** Get all entities with verification warnings (for pre-contract checks) */
export function getEntitiesRequiringVerification(): HPLegalEntity[] {
  return HP_LEGAL_ENTITIES.filter(e => e.verification_warning !== null)
}

/** Get all entities for a market */
export function getEntitiesByMarket(market: HPMarket): HPLegalEntity[] {
  return HP_LEGAL_ENTITIES.filter(e => e.market === market)
}

/** Get all unique countries HP has a legal entity in */
export function getHPCountries(): string[] {
  return [...new Set(HP_LEGAL_ENTITIES.map(e => e.iso_alpha2))].sort()
}

/** Check if HP has a local entity (vs. using a regional hub) */
export function hasLocalEntity(isoAlpha2: string): boolean {
  return HP_LEGAL_ENTITIES.some(e =>
    e.iso_alpha2.toUpperCase() === isoAlpha2.toUpperCase() &&
    e.contracting_entity !== '' &&
    !e.contracting_entity.includes('Singapore') // regional hub marker
  )
}

/** Get Care Pack invoicing details for a country */
export function getCarePackInvoicing(isoAlpha2: string): { local: boolean; entity: string | null } {
  const entity = getContractingEntity(isoAlpha2)
  return {
    local: entity?.carepack_local_invoice ?? false,
    entity: entity?.contracting_entity ?? null,
  }
}

